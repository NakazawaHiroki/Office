
#ブラウザへの入力結果定数
RES_SUCCESS                     = 0             #成功
RES_MISMATCH                    = 100           #テストリストと値が不一致
RES_NO_LEDGER                   = 101           #指定した帳票IDは存在しない
RES_ERROR                       = 102           #入力チェックの結果エラーが出た
RES_STOP                        = 200           #ユーザーの操作で中止された
RES_TAGTYPE_ERROR               = 1000          #テストリストとタグタイプが異なる
RES_TAGID_NONE                  = 1001          #指定されたタグIDはページ内に存在しない
RES_BUTTON_NONE                 = 1002          #指定されたボタンはない
RES_NO_SUCH_SELECT              = 1003          #指定された選択肢は存在しない
RES_WRONG_URL                   = 1004          #指定したURLは間違っている
RES_LOGIN_FAILURE               = 1005          #ログインできなかった
RES_NO_OPENURL                  = 1006          #指定ページを開いていない
RES_WEBDRV_INCOMPATIBLE         = 1007          #WebDriverが一致していない
RES_WEBDRV_NONE                 = 1008          #WebDriverが見つからない
RES_WEBDRV_LOADERROR            = 1009          #WebDriverのロードに失敗した
